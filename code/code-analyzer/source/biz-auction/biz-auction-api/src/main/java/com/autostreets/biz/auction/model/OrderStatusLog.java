package com.autostreets.biz.auction.model;

import com.autostreets.biz.auction.model.base.BaseOrderStatusLog;
import com.autostreets.framework.common.dal.Pagination;

public class OrderStatusLog extends BaseOrderStatusLog {

	/**
	 * 
	 */
	private static final long serialVersionUID = 4002542670980713545L;

	private Pagination pagination;

	private String prepStatusName;

	private String postStatusName;

    private String userName;

	/**
	 * @return the pagination
	 */
	public Pagination getPagination() {
		return pagination;
	}

	/**
	 * @param pagination
	 *            the pagination to set
	 */
	public void setPagination(Pagination pagination) {
		this.pagination = pagination;
	}

	/**
	 * @return the prepStatusName
	 */
	public String getPrepStatusName() {
		return prepStatusName;
	}

	/**
	 * @param prepStatusName
	 *            the prepStatusName to set
	 */
	public void setPrepStatusName(String prepStatusName) {
		this.prepStatusName = prepStatusName;
	}

	/**
	 * @return the postStatusName
	 */
	public String getPostStatusName() {
		return postStatusName;
	}

	/**
	 * @param postStatusName
	 *            the postStatusName to set
	 */
	public void setPostStatusName(String postStatusName) {
		this.postStatusName = postStatusName;
	}

    public String getUserName() {
        return userName;
    }

    public void setUserName(String userName) {
        this.userName = userName;
    }

}
