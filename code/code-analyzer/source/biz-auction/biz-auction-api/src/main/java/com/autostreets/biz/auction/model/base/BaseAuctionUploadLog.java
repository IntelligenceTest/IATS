package com.autostreets.biz.auction.model.base;

import java.io.Serializable;
import java.util.Date;

public class BaseAuctionUploadLog implements Serializable {

    private static final long serialVersionUID = 5225371253744530653L;

    private Integer id;

    private Integer auctionId;

    private Integer operType;

    private Integer memberNum;

    private Integer vehicleNum;

    private Integer dealNum;

    private Date createTime;

    private Integer createdBy;

    public BaseAuctionUploadLog(Integer id, Integer auctionId, Integer operType, Integer memberNum, Integer vehicleNum, Integer dealNum, Date createTime, Integer createdBy) {
        this.id = id;
        this.auctionId = auctionId;
        this.operType = operType;
        this.memberNum = memberNum;
        this.vehicleNum = vehicleNum;
        this.dealNum = dealNum;
        this.createTime = createTime;
        this.createdBy = createdBy;
    }

    public BaseAuctionUploadLog() {
        super();
    }

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public Integer getAuctionId() {
        return auctionId;
    }

    public void setAuctionId(Integer auctionId) {
        this.auctionId = auctionId;
    }

    public Integer getOperType() {
        return operType;
    }

    public void setOperType(Integer operType) {
        this.operType = operType;
    }

    public Integer getMemberNum() {
        return memberNum;
    }

    public void setMemberNum(Integer memberNum) {
        this.memberNum = memberNum;
    }

    public Integer getVehicleNum() {
        return vehicleNum;
    }

    public void setVehicleNum(Integer vehicleNum) {
        this.vehicleNum = vehicleNum;
    }

    public Integer getDealNum() {
        return dealNum;
    }

    public void setDealNum(Integer dealNum) {
        this.dealNum = dealNum;
    }

    public Date getCreateTime() {
        return createTime;
    }

    public void setCreateTime(Date createTime) {
        this.createTime = createTime;
    }

    public Integer getCreatedBy() {
        return createdBy;
    }

    public void setCreatedBy(Integer createdBy) {
        this.createdBy = createdBy;
    }
}